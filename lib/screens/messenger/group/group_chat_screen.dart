  textwidget(String textname, String type, String direction) {
    var texts = textname;
    if (type == "doc" || type == "video") {
      dynamic text = textname.split('#@#&');
      if (text.length == 1) {
        dynamic text = textname.split('/');
        texts = text[text.length - 1];
      } else {
        texts = text[1];
      }
    }

    return Text(
      texts.trim(),
      style: TextStyle(
          color: direction != 'send' ? Colors.black : Colors.white,
          fontSize: 16),
      textAlign: TextAlign.left,
    );
  }

